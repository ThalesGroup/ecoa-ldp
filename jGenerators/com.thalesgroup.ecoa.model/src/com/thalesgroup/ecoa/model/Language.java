package com.thalesgroup.ecoa.model;

/**
 * The list of all target languages defined by ECOA.
 */
public enum Language {

    /**
     * ISO C language (not C++)
     */
    C,
    /**
     * The Ada language (Ada 95)
     */
    ADA,
    /**
     * The C++ language
     */
    CPP,
    /**
     * The Java language
     */
    JAVA,
    /**
     * The Python language (Python 3.7)
     */
    PYTHON, 
    /**
     * Provision
     */
    RUST;

}
